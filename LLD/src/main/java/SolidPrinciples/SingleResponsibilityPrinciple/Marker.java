package SolidPrinciples.SingleResponsibilityPrinciple;

public class Marker {
    int price ;
    String color ;
    int year ;
    int name ;

    public Marker(int price, String color, int year, int name) {
        this.price = price;
        this.color = color;
        this.year = year;
        this.name = name;
    }
}
